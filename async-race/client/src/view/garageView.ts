export const garageView = (): string => {
  const startIndex = (store.pageNumber - 1) * store.items;
  const endIndex = startIndex + store.items;
  if (store.cars) {
    return store.cars
      .slice(startIndex, endIndex)
      .map((car) => {
        return roads(car);
      })
      .join("");
  } else return "";
};
